package model;

public class TravelInformation {
	
	private String journeyID;
	private String busID;
	private String passengerEmailIds;
	private String passengerNames;
	private String passengerAge;
	private String passengerGender;
	private String passengerBoardings;
	private String passengerDroppings;
	private String busSeatings;
	
	public String getBusSeatings() {
		return busSeatings;
	}
	public void setBusSeatings(String busSeatings) {
		this.busSeatings = busSeatings;
	}
	public String getJourneyID() {
		return journeyID;
	}
	public void setJourneyID(String journeyID) {
		this.journeyID = journeyID;
	}
	public String getBusID() {
		return busID;
	}
	public void setBusID(String busID) {
		this.busID = busID;
	}
	public String getPassengerEmailIds() {
		return passengerEmailIds;
	}
	public void setPassengerEmailIds(String passengerEmailIds) {
		this.passengerEmailIds = passengerEmailIds;
	}
	public String getPassengerNames() {
		return passengerNames;
	}
	public void setPassengerNames(String passengerNames) {
		this.passengerNames = passengerNames;
	}
	public String getPassengerAge() {
		return passengerAge;
	}
	public void setPassengerAge(String passengerAge) {
		this.passengerAge = passengerAge;
	}
	public String getPassengerGender() {
		return passengerGender;
	}
	public void setPassengerGender(String passengerGender) {
		this.passengerGender = passengerGender;
	}
	public String getPassengerBoardings() {
		return passengerBoardings;
	}
	public void setPassengerBoardings(String passengerBoardings) {
		this.passengerBoardings = passengerBoardings;
	}
	public String getPassengerDroppings() {
		return passengerDroppings;
	}
	public void setPassengerDroppings(String passengerDroppings) {
		this.passengerDroppings = passengerDroppings;
	}
}
